<?php



use Doctrine\ORM\Mapping as ORM;

/**
 * ContactForm
 */
class ContactForm
{
    /**
     * @var string
     */
    private $name;

    /**
     * @var integer
     */
    private $contactFormId;


    /**
     * Set name
     *
     * @param string $name
     * @return ContactForm
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Get contactFormId
     *
     * @return integer 
     */
    public function getContactFormId()
    {
        return $this->contactFormId;
    }
}
