<?php



use Doctrine\ORM\Mapping as ORM;

/**
 * Project
 */
class Project
{
    /**
     * @var string
     */
    private $name;

    /**
     * @var \DateTime
     */
    private $startDate;

    /**
     * @var \DateTime
     */
    private $scheduledEndDate;

    /**
     * @var \DateTime
     */
    private $actualEndDate;

    /**
     * @var integer
     */
    private $projectStatusId;

    /**
     * @var integer
     */
    private $priorityId;

    /**
     * @var string
     */
    private $description;

    /**
     * @var integer
     */
    private $projectName;


    /**
     * Set name
     *
     * @param string $name
     * @return Project
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set startDate
     *
     * @param \DateTime $startDate
     * @return Project
     */
    public function setStartDate($startDate)
    {
        $this->startDate = $startDate;

        return $this;
    }

    /**
     * Get startDate
     *
     * @return \DateTime 
     */
    public function getStartDate()
    {
        return $this->startDate;
    }

    /**
     * Set scheduledEndDate
     *
     * @param \DateTime $scheduledEndDate
     * @return Project
     */
    public function setScheduledEndDate($scheduledEndDate)
    {
        $this->scheduledEndDate = $scheduledEndDate;

        return $this;
    }

    /**
     * Get scheduledEndDate
     *
     * @return \DateTime 
     */
    public function getScheduledEndDate()
    {
        return $this->scheduledEndDate;
    }

    /**
     * Set actualEndDate
     *
     * @param \DateTime $actualEndDate
     * @return Project
     */
    public function setActualEndDate($actualEndDate)
    {
        $this->actualEndDate = $actualEndDate;

        return $this;
    }

    /**
     * Get actualEndDate
     *
     * @return \DateTime 
     */
    public function getActualEndDate()
    {
        return $this->actualEndDate;
    }

    /**
     * Set projectStatusId
     *
     * @param integer $projectStatusId
     * @return Project
     */
    public function setProjectStatusId($projectStatusId)
    {
        $this->projectStatusId = $projectStatusId;

        return $this;
    }

    /**
     * Get projectStatusId
     *
     * @return integer 
     */
    public function getProjectStatusId()
    {
        return $this->projectStatusId;
    }

    /**
     * Set priorityId
     *
     * @param integer $priorityId
     * @return Project
     */
    public function setPriorityId($priorityId)
    {
        $this->priorityId = $priorityId;

        return $this;
    }

    /**
     * Get priorityId
     *
     * @return integer 
     */
    public function getPriorityId()
    {
        return $this->priorityId;
    }

    /**
     * Set description
     *
     * @param string $description
     * @return Project
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string 
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * Get projectName
     *
     * @return integer 
     */
    public function getProjectName()
    {
        return $this->projectName;
    }
}
